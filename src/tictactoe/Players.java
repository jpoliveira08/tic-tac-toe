package tictactoe;

public class Players {
		//Treinando get and set
		private String player1;
		private String player2;
		//Criando método set e get
		//Método get - Obter o valor da variável declarada como private, permitir a leitura fora da classe
		//Método set - Serve para modificarmos os valores de um atributo da classe
		public void setPlayer1(String player1) {
			this.player1 = player1;
		}
		public String getPlayer1() {
			return player1;
		}
		public void setPlayer2(String player2) {
			this.player2 = player2;
		}
		public String getPlayer2() {
			return player2;
		}
		
		
		
}
